void squareOGOutput(){
   waveformSelect=7;
   double periodDelay=1/100.0;
   double dutyCycleDelay=((100-50)/100.0)*periodDelay;
   int timer1= (periodDelay*24000000/(TIMER_A_EX0_TAIDEX_7+1));
   int timer2= (dutyCycleDelay*24000000/((TIMER_A_EX0_TAIDEX_7+1)));
   TIMER_A0->CCTL[0] = TIMER_A_CCTLN_CCIE; // TACCR0 interrupt enabled
   TIMER_A0->CCTL[1] = TIMER_A_CCTLN_CCIE; // TACCR1 interrupt enabled
   TIMER_A0->EX0= TIMER_A_EX0_TAIDEX_7;
   TIMER_A0->CCR[0] = timer1-1;   // set CCR0 count   40 us
   TIMER_A0->CCR[1] = timer2-1;   // set CCR1 count   10 us
   key=255;
}
